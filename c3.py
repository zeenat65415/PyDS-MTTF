class Calculator:
# constructor
  def _init_(self,x,y):
    self.x=x
    self,y=y
#methods which are recognized by objects are called as instance method.only instance method can use instance attribute.
  def add(self):
    return self.x + self.y

  def subtract(self):
    return self.x - self.y

  def multiply(self):
    return self.x * self.y

  def divide(self):
    return self.x / self.y




